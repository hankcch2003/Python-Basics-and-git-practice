# 開啟無限迴圈，直到使用者手動輸入 0 停止
while True:
    # 讓使用者輸入訂單總金額並轉為整數
    total = int(input('請輸入訂單金額 (輸入 0 結束)：'))
    
    # 檢查結束條件：輸入 0 則跳出迴圈
    if total == 0:
        break
    
    # 初始化運費變數，預設為 0
    fee = 0
    
    # 根據金額級距判斷運費邏輯：
    if total >= 300:
        fee = 0    # 滿 300 元免運費
    elif total >= 200:
        fee = 30   # 200 ~ 299 元運費為 30 元
    else:
        fee = 60   # 未滿 200 元運費為 60 元
        
    # 輸出使用者剛才輸入的原始訂單金額
    print(f'您的訂單金額為 {total} 元')

    # 輸出經過判斷後所得到的應付運費
    print(f'運費為 {fee} 元')

    # 輸出原始金額加上運費後的最終實付總金額
    print(f'實付總額為 {total + fee} 元')