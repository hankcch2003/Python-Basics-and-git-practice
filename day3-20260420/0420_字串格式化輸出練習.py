name, age, height = 'JAMES', 25, 169.123

# 輸出純文字內容，不會將名稱視為變數進行替換
print('我的名字叫 name, 今年 age 歲, 身高: height 公分')

# 透過逗號分隔變數與字串，輸出時系統會自動在接合處產生空格
print('我的名字叫', name, ', 今年', age, '歲, 身高:', height, '公分')

# 利用加號將資料串接起來，數值型別必須先用 str() 轉為字串才能組合輸出
print('我的名字叫 ' + name + ', 今年 ' + str(age) + ' 歲, 身高: ' + str(height) + ' 公分')

# f-string 進階格式化：
# :^10s -> 輸出置中對齊，佔 10 格空間，型別為字串
# :^10f -> 輸出置中對齊，佔 10 格空間，將整數轉為浮點數輸出
# :07.1f -> 設定輸出總寬度為 7 格，不足處補 0，並取至小數第一位
print(f'我的名字叫 {name:^10s}, 今年 {age:^10f} 歲, 身高: {height:07.1f} 公分')

subject, level, score = 'python', 9, 800.2345

# :^15s -> 輸出置中對齊，佔 15 格空間
# :03d   -> 輸出整數並補零至 3 位數（常用於編號）
# : 7.1f -> 輸出總寬 7 格，取小數 1 位，前面預留空格符號位
print(f'科目: {subject:^15s}, 等級: {level:03d}, 分數: {score: 7.1f}')