text = 'python'

# 輸出索引位置為 0 的單一字元
print(text[0])   # 取得 'p'
# 輸出索引位置為 1 的單一字元
print(text[1])   # 取得 'y'
# 輸出索引位置為 5 的單一字元
print(text[5])   # 取得 'n'

# 輸出倒數第 1 個字元（負數索引從結尾算起）
print(text[-1])  # 取得 'n'
# 輸出倒數第 2 個字元
print(text[-2])  # 取得 'o'

# 輸出 50 個等號作為視覺分隔線
print("=" * 50)

# 輸出從索引 0 到 2 的子字串（包頭不包尾，不含索引 3）
print(text[0:3])   # 輸出：'pyt'
# 輸出從索引 2 到 4 的子字串
print(text[2:5])   # 輸出：'tho'
# 輸出從字串開頭到索引 2 的子字串
print(text[:3])    # 輸出：'pyt'
# 輸出從索引 2 開始直到字串結束的所有字元
print(text[2:])    # 輸出：'thon'
# 輸出完整字串內容
print(text[:])     # 輸出：'python'
# 輸出從頭到尾、每隔 2 個字元取一個的結果
print(text[::2])   # 輸出：'pto'
# 輸出步長為 -1 的結果，達成字串水平反轉
print(text[::-1])  # 輸出：'nohtyp'

