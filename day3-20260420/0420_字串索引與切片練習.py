text2 = "abcdefghijkl"

# 輸出反轉後的完整字串
print(text2[::-1])

# 輸出指定字元 'k'（利用倒數第 2 個的負索引位置）
print(text2[-2])

# 輸出從索引 3 到 5 的子字串（對應 'def'，結尾需設為 6）
print(text2[3:6])